public class Shuma {

	public static void main(String[] args) {

		//me te medhenj se 35 me te vegjel se 100 shuma e tyre
		int numerues=36;
		int shuma=0;
		
		while(numerues<100) {
			shuma+=numerues;
			numerues++;
		}
		
		System.out.println("Shuma e numrave me te medhenj se 35 dhe me te vegjel se 100 tek edhe cift eshte : "+shuma);
	}

}
